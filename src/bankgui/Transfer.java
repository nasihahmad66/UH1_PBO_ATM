    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        nama = tNama.getText();
        tujuan = tTujuan.getText();
        
        if (tRekening.getText().equals("")) {
            JOptionPane.showMessageDialog(null,"Nomor Rekening Tujuan Belum Diisi");
        }
        else if (tNilai.getText().equals("")) {
            JOptionPane.showMessageDialog(null,"Nominal Pengiriman Belum Diisi");
        }
        else if ("".equals(nama)) {
            JOptionPane.showMessageDialog(null,"Nama Penerima Belum Diisi");
        }
        else if ("".equals(tujuan)) {
            JOptionPane.showMessageDialog(null,"Keterangan Transfer Belum Diisi");
        }
        else{
         rek = Integer.parseInt(tRekening.getText());
         trf = Integer.parseInt(tNilai.getText());
            if (trf>tab) {
                JOptionPane.showMessageDialog(null,"SALDO TIDAK MENCUKUPI");
                tNilai.setText("");
            }else{
              tab-=trf;
                JOptionPane.showMessageDialog(null,"ANDA TELAH MENTRANSFER\n KE REKENING :\n"
                +rek+"\nATAS NAMA :\n"+nama+"\nSEBESAR :\nRp"+trf+"\nUNTUK KEPERLUAN :\n"+tujuan
                +"\n\nSISA SALDO ANDA :\nRp"+tab);    
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

