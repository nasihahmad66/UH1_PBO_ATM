    private void ValidasiAngka(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_ValidasiAngka
        char karakter = evt.getKeyChar();
        if(!(((karakter >= '0') && (karakter <= '9') || (karakter == KeyEvent.VK_BACK_SPACE) || (karakter == KeyEvent.VK_DELETE)))){
        getToolkit().beep();
        evt.consume();
        }
    }//GEN-LAST:event_ValidasiAngka

