    private void n11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_n11ActionPerformed
        if (tTarik.getText().equals("")) {
            JOptionPane.showMessageDialog(null,"Pastikan Inputan Sudah Terisi");
        }
        trk = Integer.parseInt(tTarik.getText());
        if (trk>tab) {
            JOptionPane.showMessageDialog(null,"Saldo Anda tidak mencukupi");
            tTarik.setText("");
            nomor="";
        }
        else if (trk<50000) {
            JOptionPane.showMessageDialog(null,"Minimal Penarikan Rp50.000");
            tTarik.setText("");
            nomor="";
        }
        else if (trk%50000!=0) {
            JOptionPane.showMessageDialog(null,"ATM hanya berisi pecahan Rp50.000");
            tTarik.setText("");
            nomor="";
        }
        else{
            tab -= trk;
            JOptionPane.showMessageDialog(null,"Sisa SALDO ANDA Rp"+tab);
        }
        
    }//GEN-LAST:event_n11ActionPerformed

